package com.example.lab1.acoesdiarias;


public class Lembrete {
    private String descricaoLembrente;
    private String Hora;
    private String Local;


    public String getDescricaoLembrente() {
        return descricaoLembrente;
    }

    public void setDescricaoLembrente(String descricaoLembrente) {
        this.descricaoLembrente = descricaoLembrente;
    }

    public String getHora() {
        return Hora;
    }

    public void setHora(String hora) {
        Hora = hora;
    }

    public String getLocal() {
        return Local;
    }

    public void setLocal(String local) {
        Local = local;
    }
}
